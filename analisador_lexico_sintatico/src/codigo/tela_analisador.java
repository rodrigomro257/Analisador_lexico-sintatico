    private void btn_analise_lexicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_analise_lexicaActionPerformed
        // TODO add your handling code here:
        File arquivo=new File("arquivo.txt");
        try {
            try (PrintWriter escrever = new PrintWriter(arquivo)) {
                escrever.print(txt_entrada.getText());
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(tela_analisador.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Reader leitor=new BufferedReader(new FileReader("arquivo.txt"));
            Lexer lexer=new Lexer(leitor);
            String resultado="";
            while(true){
                Tokens tokens=lexer.yylex();
                if(tokens==null){
                    resultado+="FIM.";
                    analise_lexica_txt.setText(resultado);
                    linha_erro=1;
                    coluna_erro=1;
                    return;
                }
                switch(tokens){
                    case ERROR:
                    resultado+="Símbolo não identificado na linha "+linha_erro+" e coluna "+coluna_erro+".\n";
                    coluna_erro++;
                    break;
                    case Enter:
                    linha_erro++;
                    coluna_erro=1;
                    break;
                    case Tab:
                    coluna_erro=coluna_erro+3;
                    case E:
                    coluna_erro++;
                    break;
                    default:
                    resultado+=lexer.lexeme+" é um "+tokens+"\n";
                    String tamanho_token=lexer.lexeme;
                    int tamanho=tamanho_token.length();
                    for(int i=0; i<tamanho; i++) coluna_erro++;
                    break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(tela_analisador.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(tela_analisador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_analise_lexicaActionPerformed

