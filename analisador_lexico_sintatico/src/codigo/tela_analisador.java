    private void btn_analise_sintaticaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_analise_sintaticaActionPerformed
        // TODO add your handling code here:
        String ST = txt_entrada.getText();
        Sintax s = new Sintax(new codigo.LexerCup(new StringReader(ST)));
        
        try {
            s.parse();
            analise_sintatica_txt.setText("Análise realizada corretamente.");
            analise_sintatica_txt.setForeground(new Color(25, 111, 61));
        } catch (Exception ex) {
            Symbol sym = s.getS();
            analise_sintatica_txt.setText("Erro de sintaxe. Linha: "+(sym.right + 1)+ 
                                          " Coluna: "+(sym.left + 1)+ 
                                          ", Texto: \""+sym.value+"\"");
            analise_sintatica_txt.setForeground(Color.red);
        }
    }//GEN-LAST:event_btn_analise_sintaticaActionPerformed

